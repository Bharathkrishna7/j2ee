package application;

public class App {

private long Appid;	
private String entre;
private String ip;
private String zon;
private String status;
   
	public long getAppid() {
		return Appid;
	}
	public void setAppid(long appid) {
		Appid = appid;
	}
	public String getEntre() {
		return entre;
	}
	public void setEntre(String entre) {
		this.entre = entre;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public String getZon() {
		return zon;
	}
	public void setZon(String zon) {
		this.zon = zon;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
}
